package com.lec.ex01_datatype;

public class FloatDoubleMain {

	public static void main(String[] args) {
		// 실수타입 - 기본형이 double
		// 정수타입 - 기본형이 int
		double d1 = 3.14;
		double d2 = 3.14D;
		double d3 = 3.14d;
		
		float f1 = 3.14f;
		float f2 = 3.14F;
		
		int i1 = 1;
		long l1 = 1l;
		long l2 = 1L;
	}

}
